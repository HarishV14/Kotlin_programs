fun main() {
    // Declare the Celsius temperature using a Double for precision
    val celsius: Double = 36.6

    // Convert Celsius to Fahrenheit using the formula
    val fahrenheit: Double = (celsius * 9 / 5) + 32

    // Print the result with a user-friendly message
    println("Temperature in Celsius: $celsius°C")
    println("Temperature in Fahrenheit: $fahrenheit°F")
}
